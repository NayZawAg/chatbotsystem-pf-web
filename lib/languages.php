function get_link($menu)
{
    if (preg_match('/index/', $_SERVER['SCRIPT_NAME'])) {
        if ($menu == 'top') {
            echo '#';
        } else {
            echo '#' . $menu;
        }
    } elseif($menu == 'top') {
        echo '/?lang=' . get_lang();
    } else {
        echo '/?lang=' . get_lang() . '#' . $menu;
    }
}

