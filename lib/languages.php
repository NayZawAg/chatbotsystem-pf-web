function get_lang() {
    $languages = array('en', 'zh-cn', 'zh-tw', 'ko');
    $lang = 'ja';
    if (isset($_GET['lang']) && in_array($_GET['lang'], $languages)) {
      $lang = $_GET['lang'];
    }
    return $lang;
}

