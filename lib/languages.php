function the_lang_word($key) {
    $word = get_lang_word($key);

    // 値が存在しない場合は null を入れる
    if (empty($word)) {
        $word = null;
    }

    // 利用規定とプライバシーポリシーは、日本語と英語のみ。（日本語以外はすべて英語）
    // cookie も日本語と英語のみ
    if (
        (
            (preg_match('/terms\.php/', $_SERVER['SCRIPT_NAME']) && preg_match('/terms/', $key)) || 
            (preg_match('/privacy\.php/', $_SERVER['SCRIPT_NAME']) && preg_match('/privacy/', $key)) ||
            (preg_match('/contact/', $_SERVER['SCRIPT_NAME']) && preg_match('/contact/', $key)) ||
            (preg_match('/contact/', $_SERVER['SCRIPT_NAME']) && preg_match('/quiry/', $key)) ||
            preg_match('/cookie/', $key)
        )
        && get_lang() != 'ja'
    ) { 
      if (isset(get_word_list()[get_user_type()]['en'][$key])){
        $word = get_word_list()[get_user_type()]['en'][$key];
      }
       
    }

    echo $word;
}

function get_lang_word($key) {
    $lang_word = null;
    if (isset(get_lang_words()[$key])) {
      $lang_word = get_lang_words()[$key];
    }
    return $lang_word;
}

function get_user_type()
